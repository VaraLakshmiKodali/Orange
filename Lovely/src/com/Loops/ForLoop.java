package com.Loops;

public class ForLoop 
{
	public static void main(String[] args) 
	{
		for(int a=1;a<3;a++)
		{
			System.out.println(a+"Selenium");
		}
		System.out.println(" ****** ");
		for(int i=2;i<=8;i=i+2)
		{
			System.out.println(i+"Testing");
		}
		System.out.println(" ******** ");
		for(int j=5;j>1;j--)
		{
			System.out.println(j+"Live Tech");
		}
		System.out.println(" ****** ");
		for(int k=-2;k>=-5;k--)
		{
			System.out.println(k+"Java");
		}
		for(int p=-18;p>-20;p=p-2)
		{
			System.out.println(p+"Load Runner");
		}
		System.out.println(" *********** ");
		for(int l=-23;l>-32;l=l-3)
		{
			System.out.println(l+"Jmeter");
		}
	}
}
